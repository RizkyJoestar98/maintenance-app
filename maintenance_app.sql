CREATE TRIGGER `trg_update_material_stock` AFTER INSERT ON `tbl_transaction` FOR EACH ROW BEGIN
    IF NEW.transaction_type = 'GR' THEN
        -- Jika transaksi adalah penerimaan barang (GR), tambahkan stok
        UPDATE tbl_material
        SET qty_stock = qty_stock + NEW.quantity
        WHERE code_material = NEW.code_material;
    ELSEIF NEW.transaction_type = 'GI' THEN
        -- Jika transaksi adalah pengeluaran barang (GI), kurangi stok
        UPDATE tbl_material
        SET qty_stock = qty_stock - NEW.quantity
        WHERE code_material = NEW.code_material;
    END IF;
END
$$
DELIMITER ;
DELIMITER $$
CREATE TRIGGER `trg_update_material_stock_after_update` AFTER UPDATE ON `tbl_transaction` FOR EACH ROW BEGIN
    DECLARE old_qty INT;
    DECLARE new_qty INT;
    DECLARE diff_qty INT;

    -- Mengambil nilai lama dan baru dari kolom quantity
    SET old_qty = OLD.quantity;
    SET new_qty = NEW.quantity;

    -- Menghitung selisih antara nilai lama dan baru
    SET diff_qty = new_qty - old_qty;

    -- Update stok barang berdasarkan selisih quantity
    IF NEW.transaction_type = 'GR' THEN
        -- Jika transaksi adalah penerimaan barang (GR), tambahkan selisih ke stok
        UPDATE tbl_material
        SET qty_stock = qty_stock + diff_qty
        WHERE code_material = NEW.code_material;
    ELSEIF NEW.transaction_type = 'GI' THEN
        -- Jika transaksi adalah pengeluaran barang (GI), kurangi selisih dari stok
        UPDATE tbl_material
        SET qty_stock = qty_stock - diff_qty
        WHERE code_material = NEW.code_material;
    END IF;
END
$$
